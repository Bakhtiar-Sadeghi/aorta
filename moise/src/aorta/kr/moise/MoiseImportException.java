public class MoiseImportException extends Exception {

	public MoiseImportException() {
	}

	public MoiseImportException(String message) {
		super(message);
	}

	public MoiseImportException(String message, Throwable cause) {
		super(message, cause);
	}

	public MoiseImportException(Throwable cause) {
		super(cause);
	}
	
}
