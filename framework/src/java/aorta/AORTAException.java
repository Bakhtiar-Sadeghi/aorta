package aorta;

public class AORTAException extends Exception {

	public AORTAException() {
		super();
	}

	public AORTAException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public AORTAException(String arg0) {
		super(arg0);
	}

	public AORTAException(Throwable arg0) {
		super(arg0);
	}

	private static final long serialVersionUID = -7339281194469901540L;

}
